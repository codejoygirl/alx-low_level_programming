#include <stdlib.h>

/**
 * add - will add two integers
 * @a: the first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - will subtract two integers
 * @a: the first int
 * @b: second int
 * Return: substract the final result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - will divide two integers
 * @a: the first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - will multiply two integers
 * @a: the first int
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - mod of  two integers
 * @a: the first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a + b);
}
